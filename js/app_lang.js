/////////////////////
// DETECT LANGUAGE //
/////////////////////
function filterLocale(locale) {
	//SAFETY
	if (!(langArray).test(locale)) {
		locale = 'en';
	}
	//multiple codes
	if (locale == 'in') {
		locale = 'id';
	}
	if (locale == 'no') {
		locale = 'nb';
	}
	//chinese
	if (locale == 'zh') {
		if ((/tw|hk|mo|hant/i).test(userLang)) {
			locale = 'zt';
		}
	}
	//VIEWPORT RESIZE
	return locale;
}
////////////
// DEFINE //
////////////
